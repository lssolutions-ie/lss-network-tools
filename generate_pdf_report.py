def render_interface_info(pdf, data):
    pdf.subsection_title("1. Interface Network Info")
    for i, (k, v) in enumerate([
        ("Interface",    data.get("interface")),
        ("IP Address",   data.get("ip_address")),
        ("Subnet Mask",  data.get("subnet")),
        ("Network Range",data.get("network")),
        ("Gateway",      data.get("gateway")),
        ("MAC Address",  data.get("mac_address")),
    ]):
        pdf.kv(k, v or "unknown", shade=i % 2 == 0)
    if data.get("is_vm"):
        vm_plat = data.get("vm_platform") or "unknown hypervisor"
        pdf.note(f"Running inside a virtual machine ({vm_plat}). Results reflect the virtualised network stack; physical-layer findings (e.g. Wi-Fi, VLAN tagging) may not be observable from within a VM.")


def render_speed_test(pdf, data):
    pdf.subsection_title("2. Internet Speed Test")
    srv   = (data.get("servers") or [{}])[0]
    ping  = srv.get("ping_ms")
    dl    = srv.get("download_mbps")
    ul    = srv.get("upload_mbps")
    for i, (k, v) in enumerate([
        ("Public IP",   srv.get("public_ip") or data.get("public_ip")),
        ("Test Server", srv.get("test_server") or srv.get("server_name")),
        ("Ping",        f"{ping} ms"   if ping is not None else "unknown"),
        ("Download",    f"{dl} Mbps"   if dl   is not None else "unknown"),
        ("Upload",      f"{ul} Mbps"   if ul   is not None else "unknown"),
    ]):
        pdf.kv(k, v or "unknown", shade=i % 2 == 0)
    if m := data.get("methodology"):
        pdf.note(m)


def render_gateway(pdf, data):
    pdf.subsection_title("3. Gateway Details")
    ports = data.get("open_ports") or []
    pdf.kv("Gateway IP",      data.get("gateway_ip", "unknown"), shade=False)
    pdf.kv("Open Port Count", str(len(ports)),                   shade=True)
    pdf.kv("Open TCP Ports",  ", ".join(str(p) for p in ports) or "none", shade=False)
    if scope := data.get("scan_scope"):
        pdf.note(f"Scan scope: {scope}")


def render_dhcp(pdf, data):
    pdf.subsection_title("4. DHCP Network Scan")
    relay_raw = data.get("relay_sources_seen") or []
    relay = ", ".join(relay_raw) or "none"
    for i, (k, v) in enumerate([
        ("Discovery Attempts",   data.get("discovery_attempts")),
        ("Responders Observed",  data.get("dhcp_responders_observed", 0)),
        ("Unique Offers",        data.get("offers_observed", 0)),
        ("Raw Offers Captured",  data.get("raw_offers_observed", 0)),
        ("Rogue DHCP Suspected", data.get("rogue_dhcp_suspected", False)),
        ("Relay / Proxy Sources",relay),
    ]):
        pdf.kv(k, v, shade=i % 2 == 0)
    responder_ips = {srv.get("ip") for srv in (data.get("servers") or [])}
    relay_only = [ip for ip in relay_raw if ip not in responder_ips]
    if relay_only:
        pdf.note(f"Relay/proxy only (no DHCP offers issued): {', '.join(relay_only)}")
    for ip in data.get("suspected_rogue_servers") or []:
        pdf.set_font("Helvetica", "", 8)
        pdf.set_text_color(*C_HGH)
        pdf.cell(0, 5, safe(f"  ! Suspected rogue responder: {ip}"), new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*C_DGR)
    servers = data.get("servers") or []
    if servers:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 8)
        pdf.cell(0, 5, f"  DHCP Responders ({len(servers)}):", new_x="LMARGIN", new_y="NEXT")
        for srv in servers:
            ports = ", ".join(str(p) for p in (srv.get("open_ports") or [])) or "none"
            pdf.set_font("Helvetica", "", 7)
            pdf.set_text_color(*C_MGR)
            pdf.set_x(pdf.l_margin + 4)
            pdf.multi_cell(
                166, 4,
                safe(f"{srv.get('ip','?')}  |  Class: {srv.get('classification','unknown')}"
                     f"  |  Offers: {srv.get('offers_observed',0)}"
                     f"  |  Rogue: {srv.get('suspected_rogue', False)}"
                     f"  |  Ports: {ports}"),
                new_x="LMARGIN", new_y="NEXT",
            )
        pdf.set_text_color(*C_DGR)


def render_dhcp_response_time(pdf, data):
    pdf.subsection_title("5. DHCP Response Time")
    iface    = data.get("interface")         or "unknown"
    server   = data.get("server_ip")         or "unknown"
    probes   = data.get("probe_count",        0)
    responded= data.get("responded_count",    0)
    loss     = data.get("packet_loss_percent",0)
    avg_ms   = data.get("avg_ms")
    min_ms   = data.get("min_ms")
    max_ms   = data.get("max_ms")
    ind      = data.get("indicators")        or {}
    is_wifi  = data.get("is_wifi",            False)

    iface_label = f"{iface}  (Wi-Fi)" if is_wifi else iface
    pdf.kv("Interface",       iface_label,                                             shade=False)
    pdf.kv("DHCP Server",     server,                                                  shade=True)
    pdf.kv("Probes / Replies",f"{probes} / {responded}",                              shade=False)
    pdf.kv("Packet Loss",     f"{loss}%",                                              shade=True)
    pdf.kv("Min Latency",     f"{min_ms} ms" if min_ms is not None else "N/A",        shade=False)
    pdf.kv("Avg Latency",     f"{avg_ms} ms" if avg_ms is not None else "N/A",        shade=True)
    pdf.kv("Max Latency",     f"{max_ms} ms" if max_ms is not None else "N/A",        shade=False)
    pdf.kv_flag("Slow Response", ind.get("slow_response", False),                     shade=True)
    pdf.kv_flag("Packet Loss",   ind.get("high_loss",     False),                     shade=False)

    if is_wifi:
        pdf.note("Measured over Wi-Fi — wireless adds inherent latency. Re-test on a wired connection for a reliable baseline.")
    if m := data.get("methodology"):
        pdf.note(m)

    times = data.get("response_times_ms") or []
    if times:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 8)
        pdf.set_text_color(*C_DGR)
        pdf.cell(0, 5, safe("  Per-Probe Results:"), new_x="LMARGIN", new_y="NEXT")
        for i, t in enumerate(times):
            val = f"{t} ms" if t is not None else "no response"
            col = C_HGH if t is None else C_DGR
            pdf.set_font("Helvetica", "", 7)
            pdf.set_text_color(*col)
            pdf.cell(0, 4, safe(f"    Probe {i+1}: {val}"), new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*C_DGR)


