def render_dhcp(pdf, data):
    pdf.subsection_title("4. DHCP Network Scan")
    relay_raw = data.get("relay_sources_seen") or []
    relay = ", ".join(relay_raw) or "none"
    for i, (k, v) in enumerate([
        ("Discovery Attempts",   data.get("discovery_attempts")),
        ("Responders Observed",  data.get("dhcp_responders_observed", 0)),
        ("Unique Offers",        data.get("offers_observed", 0)),
        ("Raw Offers Captured",  data.get("raw_offers_observed", 0)),
        ("Rogue DHCP Suspected", data.get("rogue_dhcp_suspected", False)),
        ("Relay / Proxy Sources",relay),
    ]):
        pdf.kv(k, v, shade=i % 2 == 0)
    responder_ips = {srv.get("ip") for srv in (data.get("servers") or [])}
    relay_only = [ip for ip in relay_raw if ip not in responder_ips]
    if relay_only:
        pdf.note(f"Relay/proxy only (no DHCP offers issued): {', '.join(relay_only)}")
    for ip in data.get("suspected_rogue_servers") or []:
        pdf.set_font("Helvetica", "", 8)
        pdf.set_text_color(*C_HGH)
        pdf.cell(0, 5, safe(f"  ! Suspected rogue responder: {ip}"), new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*C_DGR)
    servers = data.get("servers") or []
    if servers:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 8)
        pdf.cell(0, 5, f"  DHCP Responders ({len(servers)}):", new_x="LMARGIN", new_y="NEXT")
        for srv in servers:
            ports = ", ".join(str(p) for p in (srv.get("open_ports") or [])) or "none"
            pdf.set_font("Helvetica", "", 7)
            pdf.set_text_color(*C_MGR)
            pdf.set_x(pdf.l_margin + 4)
            pdf.multi_cell(
                166, 4,
                safe(f"{srv.get('ip','?')}  |  Class: {srv.get('classification','unknown')}"
                     f"  |  Offers: {srv.get('offers_observed',0)}"
                     f"  |  Rogue: {srv.get('suspected_rogue', False)}"
                     f"  |  Ports: {ports}"),
                new_x="LMARGIN", new_y="NEXT",
            )
        pdf.set_text_color(*C_DGR)


def render_generic_scan(pdf, num, title, data):
    pdf.subsection_title(f"{num}. {title}")
    network    = data.get("network") or "unknown"
    scan_ports = data.get("scan_ports") or "unknown"
    servers    = data.get("servers") or []
    pdf.kv("Network Range",  network,    shade=False)
    pdf.kv("Scanned Ports",  scan_ports, shade=True)
    pdf.kv("Servers Found",  len(servers), shade=False)
    if not servers:
        pdf.note("No hosts detected.")
        return
    pdf.ln(1)
    for srv in servers:
        ip       = srv.get("ip", "unknown")
        ports    = ", ".join(str(p) for p in (srv.get("open_ports") or [])) or "none"
        services = ", ".join(srv.get("detected_services") or []) or "unknown"
        pdf.set_font("Helvetica", "B", 8)
        pdf.set_text_color(*C_DGR)
        pdf.cell(0, 5, safe(f"  {ip}"), new_x="LMARGIN", new_y="NEXT")
        pdf.set_font("Helvetica", "", 7)
        pdf.set_text_color(*C_MGR)
        pdf.cell(0, 4, safe(f"    Ports: {ports}   |   Services: {services}"), new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(*C_DGR)


def render_stress_test(pdf, num, label, data):
    pdf.subsection_title(f"{num}. {label}")
    target     = data.get("gateway") or data.get("target_ip") or data.get("gateway_ip", "unknown")
    ind        = data.get("indicators") or {}
    ss         = data.get("stage_status") or {}
    baseline   = data.get("baseline")         or {}
    jitter     = data.get("jitter_test")      or {}
    large      = data.get("large_packet_test") or {}
    sustained  = data.get("sustained_test")   or {}
    recovery   = data.get("recovery")         or {}

    base_avg = baseline.get("avg_latency_ms")
    sust_avg = sustained.get("avg_latency_ms")

    pdf.kv("Target",         target,                                                    shade=False)
    pdf.kv("Baseline Avg",   f"{base_avg} ms" if base_avg is not None else "unknown",  shade=True)
    pdf.kv("Sustained Avg",  f"{sust_avg} ms" if sust_avg is not None else "unknown",  shade=False)
    pdf.kv_flag("High Jitter",        ind.get("high_jitter",        False), shade=True)
    pdf.kv_flag("Latency Under Load", ind.get("latency_under_load", False), shade=False)
    pdf.kv_flag("Packet Loss",        ind.get("packet_loss",        False), shade=True)
    pdf.kv_flag("Slow Recovery",      ind.get("slow_recovery",      False), shade=False)

    # Stage breakdown table
    # (avg, is_stddev): Jitter reports std deviation, not avg latency
    stage_rows = [
        ("Baseline",     ss.get("baseline",    "?"), baseline.get("avg_latency_ms"),  None,                              False),
        ("Jitter",       ss.get("jitter",      "?"), jitter.get("stddev_ms"),         jitter.get("packet_loss_percent"), True),
        ("Large Packet", ss.get("large_packet","?"), large.get("avg_latency_ms"),     large.get("packet_loss_percent"),  False),
        ("Sustained",    ss.get("sustained",   "?"), sustained.get("avg_latency_ms"), sustained.get("packet_loss_percent"), False),
        ("Recovery",     ss.get("recovery",    "?"), recovery.get("avg_latency_ms"),  None,                              False),
    ]
    # Column widths: 55 + 20 + 50 + 45 = 170
    COL = (55, 20, 50, 45)
    pdf.ln(3)
    # Header row
    pdf.set_fill_color(*C_NAV)
    pdf.set_text_color(*C_WHT)
    pdf.set_font("Helvetica", "B", 7)
    pdf.set_x(pdf.l_margin)
    pdf.cell(COL[0], 5, "  Stage",   fill=True)
    pdf.cell(COL[1], 5, "Status",    fill=True, align="C")
    pdf.cell(COL[2], 5, "Avg",       fill=True, align="R")
    pdf.cell(COL[3], 5, "Loss",      fill=True, align="R", new_x="LMARGIN", new_y="NEXT")
    for i, (stage_name, status, avg, loss, is_stddev) in enumerate(stage_rows):
        row_y = pdf.get_y()
        if i % 2 == 0:
            pdf.set_fill_color(*C_LGR)
            pdf.rect(pdf.l_margin, row_y, 170, 5, "F")
        avg_str  = (f"{avg} ms (\u03c3)" if is_stddev else f"{avg} ms") if avg is not None else "--"
        loss_str = f"{loss}%"   if loss is not None else "--"
        status_s = str(status) if status else "?"
        pdf.set_font("Helvetica", "", 7)
        pdf.set_text_color(*C_DGR)
        pdf.set_x(pdf.l_margin)
        pdf.cell(COL[0], 5, safe(f"  {stage_name}"))
        if status_s.lower() == "ok":
            pdf.set_text_color(*C_ADV)
        elif status_s.lower() in ("warn", "warning", "fail", "failed"):
            pdf.set_text_color(*C_HGH)
        pdf.cell(COL[1], 5, safe(status_s), align="C")
        pdf.set_text_color(*C_DGR)
        pdf.cell(COL[2], 5, safe(avg_str),  align="R")
        pdf.cell(COL[3], 5, safe(loss_str), align="R", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(*C_DGR)


def render_vlan_trunk(pdf, data):
    pdf.subsection_title("10. VLAN / Trunk Detection")
    vlan_ids = data.get("observed_vlan_ids") or []
    ind      = data.get("indicators") or {}
    for i, (k, v) in enumerate([
        ("Interface",            data.get("interface")),
        ("Tagged Frames Observed",data.get("tagged_frames_observed", False)),
        ("Observed VLAN IDs",    ", ".join(str(v) for v in vlan_ids) or "none"),
        ("Trunk Port Suspected", ind.get("trunk_port_suspected", False)),
        ("CDP/LLDP Exposure",    ind.get("cdp_exposed",          False)),
        ("Multiple VLANs Visible",ind.get("multiple_vlans_visible", False)),
    ]):
        pdf.kv(k, v, shade=i % 2 == 0)

    cdp  = data.get("cdp_neighbours")  or []
    lldp = data.get("lldp_neighbours") or []

    if cdp:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 8)
        pdf.cell(0, 5, safe(f"  CDP Neighbours ({len(cdp)}):"), new_x="LMARGIN", new_y="NEXT")
        for n in cdp:
            vlan = str(n.get("native_vlan")) if n.get("native_vlan") is not None else "unknown"
            pdf.set_font("Helvetica", "", 7)
            pdf.set_text_color(*C_MGR)
            pdf.cell(
                0, 4,
                safe(
                    f"    {n.get('device_id','?')}  |  {n.get('platform','?')}"
                    f"  |  Port: {n.get('port_id','?')}  |  Native VLAN: {vlan}"
                    f"  |  Duplex: {n.get('duplex','?')}"
                ),
                new_x="LMARGIN", new_y="NEXT",
            )
        pdf.set_text_color(*C_DGR)
    else:
        pdf.note("CDP Neighbours: none detected")

    if lldp:
        pdf.ln(2)
        pdf.set_font("Helvetica", "B", 8)
        pdf.cell(0, 5, safe(f"  LLDP Neighbours ({len(lldp)}):"), new_x="LMARGIN", new_y="NEXT")
        for n in lldp:
            pdf.set_font("Helvetica", "", 7)
            pdf.set_text_color(*C_MGR)
            pdf.cell(
                0, 4,
                safe(
                    f"    {n.get('system_name','?')}  |  Chassis: {n.get('chassis_id','?')}"
                    f"  |  Port: {n.get('port_id','?')}"
                ),
                new_x="LMARGIN", new_y="NEXT",
            )
        pdf.set_text_color(*C_DGR)
    else:
        pdf.note("LLDP Neighbours: none detected")

    probe = data.get("double_tag_probe", "not attempted")
    pdf.kv("Double-Tag Probe", probe, shade=False)


