#!/usr/bin/env python3
"""LSS Network Tools -- PDF Report Generator"""

import sys
import os
import json
from pathlib import Path

try:
    from fpdf import FPDF
except ImportError:
    print("fpdf2 is not installed. Install with: pip3 install fpdf2", file=sys.stderr)
    sys.exit(1)

# ── Colour palette ─────────────────────────────────────────────────────────
C_NAV  = (26,  42,  74)    # dark navy
C_WHT  = (255, 255, 255)   # white
C_LGR  = (245, 245, 245)   # light gray (alt rows)
C_DGR  = (33,  33,  33)    # near-black body text
C_MGR  = (117, 117, 117)   # muted / secondary text
C_SBL  = (232, 240, 254)   # section sub-header bg (light blue)
C_HGH  = (211, 47,  47)    # red   -- HIGH
C_WRN  = (245, 124,  0)    # orange -- WARNING
C_INF  = (25,  118, 210)   # blue  -- INFO
C_ADV  = (56,  142,  60)   # green -- ADVICE

SEV_COLORS = {
    "high":    C_HGH,
    "warning": C_WRN,
    "info":    C_INF,
    "advice":  C_ADV,
}


# ── PDF class ──────────────────────────────────────────────────────────────
class Report(FPDF):
    def __init__(self, client, location, note, date_stamp, prepared_by, logo_path):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.client      = client
        self.location    = location
        self.run_note    = note
        self.date_stamp  = date_stamp
        self.prepared_by = prepared_by
        self.logo_path   = logo_path
        self._cover_done = False
        self.set_auto_page_break(auto=True, margin=20)
        self.set_margins(20, 20, 20)

    def header(self):
        if not self._cover_done:
            return
        self.set_fill_color(*C_NAV)
        self.rect(0, 0, 210, 12, "F")
        self.set_font("Helvetica", "B", 7)
        self.set_text_color(*C_WHT)
        self.set_xy(8, 3)
        self.cell(
            0, 6,
            safe(f"LSS Network Tools  |  {self.client}  /  {self.location}  |  {self.date_stamp}"),
            align="L",
        )
        self.set_text_color(*C_DGR)
        self.set_y(18)

    def footer(self):
        if not self._cover_done:
            return
        self.set_y(-13)
        self.set_font("Helvetica", "I", 7)
        # Cover page has a navy strip at the bottom — use white so the text is readable
        self.set_text_color(*C_WHT if self.page_no() == 1 else C_MGR)
        self.cell(
            0, 6,
            safe(f"Page {self.page_no()}  --  Generated by LSS Network Tools (LS Solutions Software)"),
            align="C",
        )

    # ── Cover page ──────────────────────────────────────────────────────────
    def cover(self):
        self.set_auto_page_break(False)   # prevent any cover element triggering a new page

        C_ACC  = (74, 144, 226)   # light blue accent
        C_DIM  = (160, 190, 230)  # dimmed white for subtitle
        C_NVL  = (38,  60, 100)   # slightly lighter navy for node fills
        NAVY_H = 162              # mm — navy band height (~55% of A4)

        # ── Full-width navy band ──────────────────────────────────────────
        self.set_fill_color(*C_NAV)
        self.rect(0, 0, 210, NAVY_H, "F")

        # ── Decorative network topology (lower-right of navy band) ───────
        nodes = [
            (136, 108), (154,  97), (174, 112), (170, 132),
            (150, 143), (188, 101), (128, 136), (192, 124),
        ]
        edges = [(0,1),(1,2),(2,5),(2,3),(3,4),(0,4),(1,4),(5,7),(2,7),(3,7),(6,0),(6,4)]
        self.set_draw_color(*C_ACC)
        self.set_line_width(0.35)
        for a, b in edges:
            ax, ay = nodes[a]; bx, by = nodes[b]
            self.line(ax, ay, bx, by)
        # Node circles — outer ring in accent, filled with lighter navy
        for nx, ny in nodes:
            self.set_fill_color(*C_NVL)
            self.ellipse(nx - 2.8, ny - 2.8, 5.6, 5.6, "FD")
        # Centre dot in accent colour
        self.set_fill_color(*C_ACC)
        for nx, ny in nodes:
            self.ellipse(nx - 1.2, ny - 1.2, 2.4, 2.4, "F")

        # Horizontal accent line separating logo from title
        DIV_Y = 72
        self.set_draw_color(*C_ACC)
        self.set_line_width(1.0)
        self.line(20, DIV_Y, 190, DIV_Y)

        # ── Logo (centered above divider) ────────────────────────────────
        logo_rendered = False
        if self.logo_path and os.path.exists(self.logo_path):
            try:
                lw = 44
                lh = lw * (810 / 912)          # preserve aspect ratio
                self.image(self.logo_path, x=(210 - lw) / 2, y=15, w=lw, h=lh)
                logo_rendered = True
            except Exception:
                pass
        if not logo_rendered:
            self.set_font("Helvetica", "B", 30)
            self.set_text_color(*C_WHT)
            self.set_xy(0, 25)
            self.cell(210, 16, "LSS", align="C")

        # ── Report title ─────────────────────────────────────────────────
        self.set_font("Helvetica", "B", 26)
        self.set_text_color(*C_WHT)
        self.set_xy(0, DIV_Y + 7)
        self.cell(210, 13, "NETWORK AUDIT REPORT", align="C")

        # ── Subtitle ─────────────────────────────────────────────────────
        self.set_font("Helvetica", "", 10)
        self.set_text_color(*C_DIM)
        self.set_xy(0, DIV_Y + 22)
        self.cell(210, 6, safe("LS Solutions Software  \u2014  LSS Network Tools"), align="C")

        # ── Metadata card (white area below navy band) ───────────────────
        rows = [("Client", self.client), ("Location", self.location)]
        if self.run_note:
            rows.append(("Note", self.run_note))
        rows.append(("Date", self.date_stamp))
        if self.prepared_by:
            rows.append(("Prepared By", self.prepared_by))

        CARD_L   = 22
        CARD_W   = 166
        CARD_TOP = NAVY_H + 10
        LABEL_W  = 44          # key column width
        VAL_W    = CARD_W - LABEL_W - 14   # value column width (108 mm, 4 mm right padding)
        LINE_H   = 5
        V_PAD    = 3           # vertical padding above/below value text

        # Pre-measure each row's height so card outline is drawn at the right size
        self.set_font("Helvetica", "", 9)
        row_heights = []
        for k, v in rows:
            val_str = safe(str(v or ""))
            words   = val_str.split()
            lines, lw = 1, 0.0
            for word in words:
                ww = self.get_string_width(word + " ")
                if lw + ww > VAL_W - 2:
                    lines += 1
                    lw = ww
                else:
                    lw += ww
            row_heights.append(max(9, lines * LINE_H + V_PAD * 2))

        CARD_H = sum(row_heights) + 8

        # Card shadow (slight offset rectangle)
        self.set_fill_color(210, 215, 225)
        self.rect(CARD_L + 1.5, CARD_TOP + 1.5, CARD_W, CARD_H, "F")

        # Card background
        self.set_fill_color(250, 251, 254)
        self.set_draw_color(*C_NAV)
        self.set_line_width(0.4)
        self.rect(CARD_L, CARD_TOP, CARD_W, CARD_H, "FD")

        # Left navy accent bar
        self.set_fill_color(*C_NAV)
        self.rect(CARD_L, CARD_TOP, 4, CARD_H, "F")

        y = CARD_TOP + 4
        for i, ((k, v), row_h) in enumerate(zip(rows, row_heights)):
            if i % 2 == 1:
                self.set_fill_color(238, 242, 250)
                self.rect(CARD_L + 4, y, CARD_W - 4, row_h, "F")
            # Key label — vertically centred in the row
            mid_y = y + (row_h - LINE_H) / 2
            self.set_font("Helvetica", "B", 9)
            self.set_text_color(*C_NAV)
            self.set_xy(CARD_L + 10, mid_y)
            self.cell(LABEL_W, LINE_H, safe(k + ":"), align="L")
            # Value — multi_cell, top-padded, clips within VAL_W
            self.set_font("Helvetica", "", 9)
            self.set_text_color(*C_DGR)
            self.set_xy(CARD_L + 10 + LABEL_W, y + V_PAD)
            self.multi_cell(VAL_W, LINE_H, safe(str(v or "")), align="L",
                            new_x="LMARGIN", new_y="NEXT")
            y += row_h

        # ── Confidentiality note (above strip) ───────────────────────────
        self.set_font("Helvetica", "I", 7.5)
        self.set_text_color(*C_MGR)
        self.set_xy(20, 258)
        self.cell(170, 5,
            "This document is confidential and intended solely for the use of the named client.",
            align="C")

        # ── Confidentiality strip (bottom of page) ───────────────────────
        self.set_fill_color(*C_NAV)
        self.rect(0, 268, 210, 29, "F")
        # Top accent line on strip
        self.set_draw_color(*C_ACC)
        self.set_line_width(0.8)
        self.line(0, 268, 210, 268)
        self.set_font("Helvetica", "B", 8)
        self.set_text_color(*C_WHT)
        self.set_xy(0, 275)
        self.cell(210, 5, safe(f"CONFIDENTIAL  \u2014  Prepared for {self.client}"), align="C")
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*C_DIM)
        self.set_xy(0, 282)
        self.cell(210, 5, "Not for distribution beyond the named recipient", align="C")

        self.set_auto_page_break(True, margin=20)
        self._cover_done = True

    # ── Layout helpers ──────────────────────────────────────────────────────
    def section_title(self, title):
        self.ln(5)
        self.set_fill_color(*C_NAV)
        self.set_text_color(*C_WHT)
        self.set_font("Helvetica", "B", 10)
        self.cell(0, 8, safe(f"  {title}"), fill=True, new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)
        self.ln(2)

    def subsection_title(self, title):
        self.ln(3)
        self.set_fill_color(*C_SBL)
        self.set_text_color(*C_NAV)
        self.set_font("Helvetica", "B", 9)
        self.cell(0, 7, safe(f"  {title}"), fill=True, new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)
        self.ln(1)

    def kv(self, key, value, shade=False):
        self.set_x(self.l_margin)
        if shade:
            self.set_fill_color(*C_LGR)
        self.set_font("Helvetica", "B", 8)
        self.cell(52, 5, safe(f"  {key}"), fill=shade)
        self.set_font("Helvetica", "", 8)
        val_w = self.w - self.l_margin - self.r_margin - 52
        self.multi_cell(val_w, 5, safe(str(value) if value is not None else "--"), fill=shade,
                        new_x="LMARGIN", new_y="NEXT")

    def finding_row(self, severity, title, detail, shade=False):
        color = SEV_COLORS.get(severity.lower(), C_MGR)
        if shade:
            self.set_fill_color(*C_LGR)
            self.rect(self.l_margin, self.get_y(), 170, 5, "F")
        # Severity badge
        self.set_fill_color(*color)
        self.set_text_color(*C_WHT)
        self.set_font("Helvetica", "B", 7)
        self.cell(22, 5, safe(severity.upper()), fill=True, align="C")
        # Title on same line
        self.set_text_color(*C_DGR)
        self.set_font("Helvetica", "B", 8)
        self.cell(148, 5, safe(f"  {title}"), new_x="LMARGIN", new_y="NEXT")
        # Detail indented
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*C_MGR)
        self.set_x(self.l_margin + 24)
        self.multi_cell(146, 4, safe(detail), new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)
        self.ln(1)

    def hint_row(self, title, detail, shade=False):
        if shade:
            self.set_fill_color(*C_LGR)
            self.set_font("Helvetica", "B", 8)
            self.cell(170, 5, safe(f"  {title}"), fill=True, new_x="LMARGIN", new_y="NEXT")
        else:
            # Accent bar on the left for unshaded rows
            bar_y = self.get_y()
            self.set_font("Helvetica", "B", 8)
            self.cell(170, 5, safe(f"  {title}"), fill=False, new_x="LMARGIN", new_y="NEXT")
            self.set_draw_color(*C_NAV)
            self.set_line_width(0.6)
            self.line(self.l_margin, bar_y, self.l_margin, bar_y + 5)
            self.set_line_width(0.2)
        self.set_font("Helvetica", "B", 8)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*C_MGR)
        self.set_x(self.l_margin + 4)
        self.multi_cell(166, 4, safe(detail), new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)
        self.ln(1)

    def kv_flag(self, key, value, shade=False):
        """Like kv() but renders boolean True/False with colour (red/green)."""
        self.set_x(self.l_margin)
        if shade:
            self.set_fill_color(*C_LGR)
        self.set_font("Helvetica", "B", 8)
        self.cell(52, 5, safe(f"  {key}"), fill=shade)
        bool_val = value is True or str(value).lower() == "true"
        self.set_font("Helvetica", "B", 8)
        self.set_text_color(*(C_HGH if bool_val else C_ADV))
        val_w = self.w - self.l_margin - self.r_margin - 52
        self.multi_cell(val_w, 5, "Yes" if bool_val else "No",
                        fill=shade, new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)

    def note(self, text):
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(*C_MGR)
        self.cell(0, 5, safe(f"  {text}"), new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*C_DGR)


# ── Text helpers ──────────────────────────────────────────────────────────
def safe(text):
    """Transliterate common unicode chars then strip anything still outside latin-1."""
    if text is None:
        return "--"
    s = str(text)
    s = s.replace("\u2014", "--").replace("\u2013", "-")   # em/en dash
    s = s.replace("\u2019", "'").replace("\u2018", "'")    # curly single quotes
    s = s.replace("\u201c", '"').replace("\u201d", '"')    # curly double quotes
    s = s.replace("\u03c3", "stddev").replace("\u00b0", "deg")  # sigma, degree
    s = s.replace("\u2022", "*").replace("\u00a9", "(c)")  # bullet, copyright
    return s.encode("latin-1", errors="replace").decode("latin-1")


# ── Data helpers ──────────────────────────────────────────────────────────
def load_json(path):
    try:
        with open(path) as f:
            return json.load(f)
    except Exception:
        return None


def task_json_path(run_dir, manifest, task_id):
    """Return Path to the primary JSON for a given task_id, or None."""
    for t in manifest.get("tasks", []):
        try:
            if int(t.get("task_id", -1)) == task_id:
                files = t.get("json_files") or []
                if files:
                    return run_dir / files[0]
                jf = t.get("json_file")
                if jf:
                    return run_dir / jf
        except (TypeError, ValueError):
            continue
    return None


def all_task_json_paths(run_dir, manifest, task_id):
    """Return list of existing Paths for all JSON files of a multi-entry task."""
    for t in manifest.get("tasks", []):
        try:
            if int(t.get("task_id", -1)) == task_id:
                return [
                    run_dir / f
                    for f in (t.get("json_files") or [])
                    if (run_dir / f).exists()
                ]
        except (TypeError, ValueError):
            continue
    return []


