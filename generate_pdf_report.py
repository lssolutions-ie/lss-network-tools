        if   num <= 3: stripe = C_INF
        elif num <= 6: stripe = C_ADV
        elif num <= 9: stripe = C_WRN
        else:          stripe = (130, 80, 180)
        pdf.set_fill_color(*stripe)
        pdf.rect(20, row_y, 2, row_h, "F")

