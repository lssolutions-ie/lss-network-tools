def _measure_row_h(pdf, desc, col_w, line_h):
    """Return the row height needed to fit desc in col_w mm at the current font."""
    words = safe(desc).split()
    lines, line_w = 1, 0.0
    for word in words:
        w = pdf.get_string_width(word + " ")
        if line_w + w > col_w - 1:
            lines += 1
            line_w = w
        else:
            line_w += w
    return max(10.0, lines * line_h + 5)


