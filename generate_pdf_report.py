def render_interface_info(pdf, data):
    pdf.subsection_title("1. Interface Network Info")
    for i, (k, v) in enumerate([
        ("Interface",    data.get("interface")),
        ("IP Address",   data.get("ip_address")),
        ("Subnet Mask",  data.get("subnet")),
        ("Network Range",data.get("network")),
        ("Gateway",      data.get("gateway")),
        ("MAC Address",  data.get("mac_address")),
    ]):
        pdf.kv(k, v or "unknown", shade=i % 2 == 0)


